/** Get broad category for a ship type code */
export function getShipTypeCategory(code: number): string {
  if (code === 0) return "Unknown";
  if (code >= 1 && code <= 19) return "Reserved";
  if (code >= 20 && code <= 29) return "WIG";
  if (code >= 30 && code <= 39) return "Special";
  if (code >= 40 && code <= 49) return "HSC";
  if (code >= 50 && code <= 59) return "Special";
  if (code >= 60 && code <= 69) return "Passenger";
  if (code >= 70 && code <= 79) return "Cargo";
  if (code >= 80 && code <= 89) return "Tanker";
  if (code >= 90 && code <= 99) return "Other";
  return "Unknown";
}

export function getShipTypeLabel(code: number | string | null | undefined): string {
  if (code === null || code === undefined) return "Unknown";
  const n = typeof code === "string" ? parseInt(code, 10) : code;
  return SHIP_TYPE_MAP[n] ?? `Unknown (${n})`;
}

