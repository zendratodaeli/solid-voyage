/**
 * Get the flag SVG URL for an ISO3 code.
 * Falls back to case-insensitive lookup.
 */
export function getFlagUrl(flagCode: string | null | undefined): string {
  if (!flagCode) return "";
  const raw = flagCode.trim();
  if (FLAG_ICON_URLS[raw]) return FLAG_ICON_URLS[raw];
  const upper = raw.toUpperCase();
  if (FLAG_ICON_URLS[upper]) return FLAG_ICON_URLS[upper];
  return "";
}

