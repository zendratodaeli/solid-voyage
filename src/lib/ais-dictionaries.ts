/** Derive flag info from a 9-digit MMSI number */
export function getFlagFromMmsi(mmsi: string | number | null | undefined): FlagInfo | null {
  if (!mmsi) return null;
  const s = String(mmsi).trim();
  if (s.length < 3) return null;
  const mid = parseInt(s.substring(0, 3), 10);
  return MID_TO_FLAG[mid] ?? null;
}

/** Get flag info from an ISO3 code returned by the API (e.g. "PAN", "LBR") */
export function getFlagInfo(iso3: string | null | undefined): FlagInfo | null {
  if (!iso3) return null;
  const upper = iso3.toUpperCase();
  for (const info of Object.values(MID_TO_FLAG)) {
    if (info.iso3 === upper) return info;
  }
  return null;
}

